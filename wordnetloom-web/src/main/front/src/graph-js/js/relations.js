Relations.prototype.sortRelations = function(arr){
  var self = this;
  arr.sort(function(a,b){
    var aOrder = self.getOrderById(a.rel);
    var bOrder = self.getOrderById(b.rel);
    return aOrder - bOrder;
  });
};

